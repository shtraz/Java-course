public class java_operations {
    public static void main(String[] args) {
        // Побитовый сдвиг
        int a = 18;
        // 10010
        // a = a << 1;
        System.out.println(a >> 1);
        // 1001 = 9
    }

    // Присваивание: =
    // Арифметические: *, /, +, -, %, ++, --
    // Операции срванения: <, >, ==, !=, >=, <=
    // Логические операции: ||, &&, ^, !
    // Побитовые операции: <<, >>, &, |, ^
}